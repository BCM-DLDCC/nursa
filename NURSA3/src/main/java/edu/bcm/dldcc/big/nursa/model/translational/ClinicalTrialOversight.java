package edu.bcm.dldcc.big.nursa.model.translational;

import java.io.Serializable;

import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;

@Entity
public class ClinicalTrialOversight  implements Serializable {


	private static final long serialVersionUID = -6459512269926241323L;
	@Id
	@GeneratedValue(generator = "ctOversightSequencer")
	@SequenceGenerator(name = "ctOversightSequencer", sequenceName = "CTOVERSIGHT_SEQ")
	private Long id;
	
	private String name;

	/**
	 * @return the id
	 */
	public Long getId() {
		return id;
	}

	/**
	 * @param id the id to set
	 */
	public void setId(Long id) {
		this.id = id;
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

}
